public class MatchRules
{
    /// <summary>
    /// The amount of time each player has to plan their turn in seconds.
    /// </summary>
    public float planningTime = 20f;

    /// <summary>
    /// Length of the simulated turn in seconds.
    /// </summary>
    public float turnTime = 10f;

    /// <summary>
    /// How many steps are simulated per second.
    /// </summary>
    public int simulationTickrate = 60;
}